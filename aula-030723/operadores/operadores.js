const num1 = 5;
const num2 = 8;
const num3 = 4.5;

/*
    OPERADORES ARITMÉTICOS:
        + Adição
        * Multiplicação
        / Divisão
        ** Exponenciação
        % (modulo) Resto da Divisão

*/

let result;

result = num1 + num2; // 13
console.log(result);

result = num1 - num2; // -3
console.log(result);

result = num1 * num2; // 40
console.log(result);

result = num1 / num2; // 0.625
console.log(result);

result = num1 ** num2; //390625
console.log(result); 

result = num2 % num3;
console.log(result);